/*
[문제 요약]

🏠 문제:  
- 길이가 N인 아파트가 일렬로 있음 (1~N).
- 기존에 몇몇 위치(stations)에 4G 기지국이 설치돼 있고, 전파 도달 범위는 W.
- 4G를 5G로 교체하면서 전파가 닿지 않는 구간에 5G 기지국을 **최소로 추가 설치**해야 함.

📋 규칙:
- 기존 기지국은 고정.
- 새 기지국의 전파 도달 범위도 W → 설치된 위치에서 [위치-W, 위치+W]까지 전파 가능.
- 모든 아파트에 전파가 도달해야 함.
- 새로 설치할 기지국의 개수를 최소화해야 함.

📝 입력:
- N: 아파트 개수 (≤ 200,000,000)
- stations: 기존 기지국 설치 위치 배열 (≤ 10,000, 오름차순)
- W: 전파 도달 거리 (≤ 10,000)

🎯 출력:
- 새로 설치해야 하는 기지국의 최소 개수

📌 예시:
N = 11, stations = [4,11], W = 1
→ 전파가 닿지 않는 구간: [1,2], [6,7,8,9]
→ 새 기지국 3개 설치 필요 → 정답: 3

N = 16, stations = [9], W = 2
→ 전파가 닿지 않는 구간: [1~6], [12~16]
→ 새 기지국 3개 설치 필요 → 정답: 3
*/
class Solution {
    public int solution(int n, int[] stations, int w) {
        int answer = 0;
        int idx = 1;     // 현재 커버리지의 시작점
        int i = 0;       // stations 인덱스
        int coverage = 2 * w + 1;

        while (idx <= n) {
            if (i < stations.length && stations[i] - w <= idx) {
                // 현재 위치가 기존 기지국의 커버리지 안이라면
                idx = stations[i] + w + 1;  // 다음 위치로
                i++;
            } else {
                // 커버되지 않은 구간이라면 새 기지국 설치
                answer++;
                idx += coverage;
            }
        }

        return answer;
    }
}
